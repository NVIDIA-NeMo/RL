  ONLINE_EXTRA_OVERRIDES="${ONLINE_EXTRA_OVERRIDES} ++policy.megatron_cfg.env_vars.CUDA_DEVICE_MAX_CONNECTIONS=\\\"${CUDA_DEVICE_MAX_CONNECTIONS}\\\""
